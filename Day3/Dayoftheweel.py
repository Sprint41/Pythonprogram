# Prompt the user for a number representing a day of the week (1 for Monday, 2 for Tuesday, etc.). Use if-elif-else statements to print the corresponding
# day's name.
week= input("Enter name of the week:")
if week=='monday':
    print("Monday")
elif week=='tuesday':
    print("Tuesday")
elif week=='wednesday':
    print("Wednesday")
elif week=='thursday':
    print("Thursday")
elif week=='friday':
    print("Friday")
elif week=='saturday':
    print("Saturday")
elif week=='sunday':
    print("Sunday")
else:
    print("Invalid input. Please enter a valid input.")
